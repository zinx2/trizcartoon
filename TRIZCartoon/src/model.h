    void setError(const bool &m) { m_error = m; emit errorChanged(); }
	void setExtractedConcept(const QMap<int, int> &m) { m_extractedConcept = m; emit extractedConceptChanged(); }

signals:
	void idChanged();
	void nameChanged();
	void desc1Changed();
	void desc2Changed();
